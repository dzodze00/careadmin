export function StateSelector() {
  const [open, setOpen] = useState(false)
  const { selectedStates, setSelectedStates } = useStateContext()

  const handleStateToggle = (value) => {
    setSelectedStates(
      selectedStates.includes(value) ? selectedStates.filter((state) => state !== value) : [...selectedStates, value],
    )
  }

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button variant="outline" role="combobox" aria-expanded={open} className="justify-between">
          <div className="flex flex-wrap gap-1 max-w-[300px] overflow-hidden">
            {selectedStates.length === 0 ? (
              <span>Select states...</span>
            ) : (
              selectedStates.map((state) => (
                <Badge key={state} variant="secondary" className="mr-1">
                  {state}
                </Badge>
              ))
            )}
          </div>
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[300px] p-0">
        <Command>
          <CommandInput placeholder="Search states..." />
          <CommandList>
            <CommandEmpty>No state found.</CommandEmpty>
            <CommandGroup className="max-h-[300px] overflow-auto">
              {states.map((state) => (
                <CommandItem key={state.value} value={state.value} onSelect={() => handleStateToggle(state.value)}>
                  <Check
                    className={cn("mr-2 h-4 w-4", selectedStates.includes(state.value) ? "opacity-100" : "opacity-0")}
                  />
                  {state.label} ({state.value})
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  )
}
